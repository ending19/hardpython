print("I am 6'2\" tall.")   # 将字符串中的双引号转义
print('I am 6\'2" tall.')   # 将字符串中的单引号转义

tabby_cat = "\tI'M tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
